package zixin.action;

public class ActionObject {

	private String i;
	private String n;

	public ActionObject() {
	}

	public ActionObject(String i, String n) {
		super();
		this.i = i;
		this.n = n;
	}

	public String getI() {
		return i;
	}

	public void setI(String i) {
		this.i = i;
	}

	public String getN() {
		return n;
	}

	public void setN(String n) {
		this.n = n;
	}

}
